package kr.or.dto;

public class Board_Rank {
	private int r_code;
	private int w_rank;
	private int re_rank;
	
	public int getR_code() {
		return r_code;
	}
	public void setR_code(int r_code) {
		this.r_code = r_code;
	}
	public int getW_rank() {
		return w_rank;
	}
	public void setW_rank(int w_rank) {
		this.w_rank = w_rank;
	}
	public int getRe_rank() {
		return re_rank;
	}
	public void setRe_rank(int re_rank) {
		this.re_rank = re_rank;
	}
	
	
}
